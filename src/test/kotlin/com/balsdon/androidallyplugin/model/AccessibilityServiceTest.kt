package com.balsdon.androidallyplugin.model

import com.google.common.truth.Truth.assertThat
import org.junit.Test

class AccessibilityServiceTest {
    @Test
    fun no_packages_has_empty_list() {
        // given + when
        // asked to filter an empty list of packages
        val result = filterFromPackages(emptyList())

        // then
        // return an empty list of Accessibility services
        assertThat(result).isEmpty()
    }

    @Test
    fun packages_with_no_accessibility_has_empty_list() {
        // when
        // asked to filter a list of packages that has no accessibility services
        val result = filterFromPackages(packagesNoAccessibility)
        // then
        // return an empty list of Accessibility services
        assertThat(result).isEmpty()
    }

    @Test
    fun packages_with_one_accessibility_service_returns_one() {
        // when
        // asked to filter a list of packages with one accessibility service
        val result = filterFromPackages(packagesOneAccessibility)
        // then
        // return a list of Accessibility services
        assertThat(result.size).isEqualTo(1)
        assertThat(result[0].packageName).isEqualTo("com.google.android.accessibility.switchaccess")
    }

    @Test
    fun packages_with_many_services_has_list() {
        // when
        // asked to filter a list of packages with many accessibility services
        val result = filterFromPackages(packagesAllAccessibility)
        // then
        // return a list of Accessibility services
        assertThat(result.size).isEqualTo(supportedAccessibilityServiceList.size)
    }

